#Calcular promedio de 5 materias

notaMateria1 = float(input("Ingresa la nota de la materia 1: "))
notaMateria2 = float(input("Ingresa la nota de la materia 2: "))
notaMateria3 = float(input("Ingresa la nota de la materia 3: "))
notaMateria4 = float(input("Ingresa la nota de la materia 4: "))
notaMateria5 = float(input("Ingresa la nota de la materia 5: "))

promedioNotas = (notaMateria1 + notaMateria2 + notaMateria3 + notaMateria4 + notaMateria5) / 5

print(f"El promedio de las notas es: {promedioNotas}")